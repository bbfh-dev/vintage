type PackVersionRange struct {
	Min PackVersion
	Max PackVersion
}

type PackVersion struct {
	Digits [2]int
	Flag   uint8
}

func (version PackVersion) Value() any {
	if version.Digits[1] == 0 {
		return version.Digits[0]
	}

	if version.Digits[1] > 9 {
		return float64(version.Digits[0]) + float64(version.Digits[1])/100
	}
	return float64(version.Digits[0]) + float64(version.Digits[1])/10
}

func IsVersionSupported(version string) bool {
	_, ok := DataPackFormats[version]
	return ok
}

func UsesPluralFolderNames(version string) bool {
	if version == "" {
		return false
	}
	format, ok := DataPackFormats[version]
	if !ok {
		return false
	}
	return format.Digits[0] < DataPackFormats["1.21"].Digits[0]
}

